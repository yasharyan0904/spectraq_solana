// SpectraQ Vault — non-custodial trading vault on Solana.
//
// Mode 1 (live demo): pooled USDC + SOL deposits, SPL share-token accounting,
// agent-driven SOL/USDC trades on a moving-average crossover signal. USDT is
// out of scope for this build per the project owner's directive.
//
// Prompt 3 wires Arcium MPC: `request_signal_computation` queues an MPC job
// over an encrypted price window, the cluster threshold-decrypts the result,
// and `compute_ma_signal_callback` lands the plaintext signal back on chain.

